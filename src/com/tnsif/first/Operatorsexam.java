package com.tnsif.first;

public class Operatorsexam {
	
	    public static void main(String[] args) {
	        int a = 10, b = 3;

	        System.out.println("Arithmetic: " + (a + b));
	        System.out.println("Relational: " + (a > b));
	        System.out.println("Logical: " + ((a > 5) && (b < 5)));
	        System.out.println("Assignment: " + (a += b));
	        System.out.println("Unary: " + (++a));
	        System.out.println("Bitwise: " + (a & b));
	        System.out.println("Ternary: " + ((a > b) ? "a is bigger" : "b is bigger"));
	    }
	}



